/**
 * i18n — tiny, flat-keyed translation store.
 *
 * Design choices:
 *   - Single dictionary per language, keyed by dot-path strings (e.g.
 *     `nav.memories`). Keeps lookup O(1) and makes ad-hoc string
 *     interpolation trivial.
 *   - Language preference persists in localStorage; default language
 *     is inferred from `navigator.language` (zh-* → zh, else en).
 *   - Uses @preact/signals so components re-render automatically on
 *     language switch without subscription plumbing.
 *
 * Adding a key:
 *   1. Add it to both `en` and `zh` dictionaries.
 *   2. Use `t("your.key")` in a component. TypeScript enforces the
 *      key exists because `TranslationKey` is derived from `en`.
 */
