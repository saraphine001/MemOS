export type Locale = "en" | "zh";

const STORAGE_KEY = "memos.lang";

function detectDefault(): Locale {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === "en" || saved === "zh") return saved;
  } catch {
    // ignore
  }
  const nav = (typeof navigator !== "undefined" && navigator.language) || "en";
  return nav.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export const locale = signal<Locale>(detectDefault());

const table = computed<Record<string, string>>(() => (locale.value === "zh" ? zh : (en as Record<string, string>)));

/**
 * Look up a translation key. When a dynamic value is needed, pass an
 * object and the translator substitutes `{key}` placeholders:
 *
 *   t("common.selected", { n: 3 })
 */
export function t(key: TranslationKey, vars?: Record<string, string | number>): string {
  const raw = table.value[key] ?? (en as Record<string, string>)[key] ?? key;
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`));
}

export function setLocale(next: Locale): void {
  if (locale.value === next) return;
  locale.value = next;
  try {
    localStorage.setItem(STORAGE_KEY, next);
  } catch {
    // ignore
  }
  if (typeof document !== "undefined") {
    document.documentElement.setAttribute("lang", next === "zh" ? "zh-CN" : "en");
  }
}

export function toggleLocale(): void {
  setLocale(locale.value === "en" ? "zh" : "en");
}

// Initialise <html lang> once on load so screenreaders + CSS selectors
// see the right language immediately.
if (typeof document !== "undefined") {
  document.documentElement.setAttribute("lang", locale.value === "zh" ? "zh-CN" : "en");
}
